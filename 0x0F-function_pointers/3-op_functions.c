/**
 * op_mod - modulus
 * @a: int
 * @b: int
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_div - divide
 * @a: int
 * @b: int
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mul - multiply
 * @a: int
 * @b: int
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_sub - subtracts
 * @a: int
 * @b: int
 *
 * Result: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_add - add
 * @a: int
 * @b: int
 *
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
